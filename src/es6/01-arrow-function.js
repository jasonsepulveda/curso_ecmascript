// normal function
function square(num) {
    return num * num;
}
console.log(square(4));

// function with block of code
const square = (num) => {
    return num * num;
}
console.log(square(2));

// arrow function
const square = num => num * num;
console.log(square(3));

// all functions are the same, however I code use less code with some of them.